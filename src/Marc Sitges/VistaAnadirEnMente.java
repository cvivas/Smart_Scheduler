    private void AceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AceptarMouseClicked
        // TODO add your handling code here:
        boolean error1=false;
        boolean error2=false;
        boolean error3=false;
        boolean error4=false;
        boolean error5=false;

        int slotMin = 1;
        int slotMax = 1;
        Date inicio;

        textoError.setText("");
        String titulo = jTextField1.getText();  //captura el titulo
        String tema = (String) comboTema.getSelectedItem(); //captura el tema
        if(tema.equals("Ninguno"))error5=true;
        String prioridad = (String) jComboBox3.getSelectedItem();
        String descripcion = jTextPane1.getText();
        String horas = TextoHoras.getText();
        boolean laboral=jCheckBox1.isSelected();
        Calendar c = null;
        c = fechaCombo.getSelectedDate();
        inicio = c.getTime();

        String prueba = horasMin.getText();
        try{
            slotMin = Integer.parseInt(prueba);
        }catch(Exception e){
            slotMin=0;          
        }
        prueba = horasMax.getText();
        try{
            slotMax = Integer.parseInt(prueba);
        }catch(Exception e){
            slotMax=0;          
        }

        if(slotMax<slotMin) error1=true;
        
        int h=0;
        int prio;
        int resultat = 1;
        if(prioridad.equals("Alta")) prio=Constantes.Alta;
        else if(prioridad.equals("Media")) prio = Constantes.Media;
        else prio = Constantes.Baja;

        try{
            h = Integer.parseInt(horas);
        }catch(Exception e){
            h=0;           
        }

        if(titulo.isEmpty())error2=true;
        if(tema == null)error3=true;
        if(descripcion.isEmpty())error4=true;

        if(!error1 && !error2 && !error3 && !error4 && !error5){

            int idActividad = nuevaEnMente.crearActividad(titulo, descripcion, "EnMente");
            CT.crearTema(tema, titulo, idActividad);

            /*Si la creacion ha tenido exito*/
            if(idActividad>0){
                /*Si horas es superior a 0*/
                if(h>0){
                    Calendar cal = Calendar.getInstance();
                    cal.setTime (inicio);
                    cal.add (Calendar.YEAR, 1);
                    Date fin= cal.getTime();
                    nuevaEnMente.completarEnMente(prio, h, laboral, slotMin, slotMax, inicio);

                    while(resultat!=0){

                        resultat=CDA.asignacionAutomatica(idActividad, prio, h, laboral,
                            inicio, fin, slotMin, slotMax);

                        if(resultat>0){

                            inicio= (Date) CtrlAgenda.diaSiguiente(fin).clone();
                            fin = CtrlAgenda.annoSiguiente(inicio);
                            h=resultat;
                        }
                    }
                    this.dispose();
                }
                else{
                    Date fin = (Date) CDA.annoSiguiente(inicio).clone();
                    nuevaEnMente.completarEnMente(prio, h, laboral, 0, 0,inicio);
                    CtrlCrearAviso CCA = new CtrlCrearAviso();
                    while(inicio.before(fin)){
                       int idAv=CCA.crearAviso(inicio, idActividad);
                        inicio = (Date) CDA.semanaSiguiente(inicio).clone();
                    }
                    this.dispose();
                }
            }
        }
        else{
            if(error1)textoError.setText("Error en las horas minimos y maximas");
            else if(error2)textoError.setText("titulo inexistente");
            else if(error3)textoError.setText("el tema es nulo");
            else if(error4)textoError.setText("no existe descripcion");
            else if(error5)textoError.setText("selecciona un tema");
        }

        
       
}//GEN-LAST:event_AceptarMouseClicked

