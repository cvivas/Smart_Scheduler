    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if(!existe)jLabel7.setText("No hay ninguna actividad en mente en el sistema");
        else{
            String tipo = (String) tipoEliminacion.getSelectedItem();

            if(tipo.equals("Total")){
                CEA.eliminarActividadTodo(idActividadSeleccionada);
                //CDA.recuperarHoras(inicio, fin);
            }
            if(tipo.equals("Horario concreto")){

                Calendar calen = dateChooserCombo1.getSelectedDate();
                Date data = calen.getTime();
                CEA.eliminarActividadDia(idActividadSeleccionada, data);

            }
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

