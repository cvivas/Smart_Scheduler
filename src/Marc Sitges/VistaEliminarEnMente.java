    private boolean cargarEnMente(int idActividadSeleccionada) {

        Actividad a=CDA.CA3.buscarActividad(idActividadSeleccionada);
        if(a==null){
            tituloEnMente.disable();
            panelDescripcion.disable();
            return false;
        }
        EnMente modificador=(EnMente)a;

        tituloEnMente.setText(modificador.getTitulo());
        tituloEnMente.disable();
        panelDescripcion.setText(modificador.getDescripcion());
        panelDescripcion.disable();

        return true;

    }

